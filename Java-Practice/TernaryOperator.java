public class TernaryOperator{
	public static void main(String[] args) {
		//Ternary operator basically executes like:
		// (Condition)?(Expression1):(Expression2)
		//if cond is True then Exp1 is executed otherwise Exp2 is executed
		
		
		int a,b,c,d;
		a =10;
		b=20;
		c = (a>b)?a:b;
		d = (a<b)?a:b;
		System.out.println(c);
		System.out.println(d);







	}
}